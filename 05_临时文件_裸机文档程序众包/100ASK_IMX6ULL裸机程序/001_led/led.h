
#ifndef   __LED_H__
#define   __LED_H__

/**********************************************************************
 * 函数名称： led_init
 * 功能描述： 初始化LED引脚，就是把它设置为输出引脚
 * 输入参数： 无
 * 输出参数： 无
 * 返 回 值： 无
 * 修改日期        版本号     修改人	      修改内容
 * -----------------------------------------------
 * 2020/02/07	     V1.0	  韦东山	      创建
 ***********************************************************************/
void led_init(void);


/**********************************************************************
 * 函数名称： led_ctl
 * 功能描述： 设置LED状态
 * 输入参数： 
 *     on : 1-LED点亮, 0-LED熄灭
 * 输出参数： 无
 * 返 回 值： 无
 * 修改日期        版本号     修改人	      修改内容
 * -----------------------------------------------
 * 2020/02/07	     V1.0	  韦东山	      创建
 ***********************************************************************/
void led_ctl(int on);

#endif

